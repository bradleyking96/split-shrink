	public static Color crayonColor(){
		int r = 0;
		int g = 0;
		int b = 0;
		for(Color color:colors){
			r += color.getRed();
			g += color.getGreen();
			b += color.getBlue();
		}
		int numColors = colors.length;
		r /= numColors;
		g /= numColors;
		b /= numColors;
		return new Color(r, g, b);
	}
	
	public static Color random(Color...exceptions){
		boolean done = false;
		int index = 0;
		while(!done){
			index = (int) (Math.random()*colors.length);
			done = true;
			for(Color exception:exceptions){
				if(colors[index] == exception){
					done = false;
				}
			}
		}
		return colors[index];
	}
	
	public static Color randomFrom(Color...colorChoices){
		int index = 0;
		index = (int) (Math.random()*colorChoices.length);
		return colorChoices[index];
	}
